package logic;

public class Product {
    public String name;
    public Double proteins;
    public Double fats;
    public Double carbohydrates;

    public Product(String name, Double proteins, Double fats, Double carbohydrates){
        this.name = name;
        this.proteins = proteins;
        this.fats = fats;
        this.carbohydrates = carbohydrates;
    }
}
